function Draw_picture_callback(hObject, eventdata, handles)
Pict_cell = handles.Current_image;
for ii = 1:length(Pict_cell)
    im = Pict_cell{ii};
    subplot(1,length(Pict_cell)+3,ii,'Parent',handles.uipanel1)
    imagesc(im);
    colormap('bone')
    axis equal
    im_max = max(max(im));
    
    Skin_high=handles.slider1.Value * im_max;
    Skin_low=handles.slider2.Value * im_max;
    PVS_high=handles.slider3.Value* im_max;
    PVS_low=handles.slider4.Value* im_max;
    Ring_high=handles.slider5.Value* im_max;
    Ring_low=handles.slider6.Value* im_max;
    
    skinmask  = im*0;
    skinmask(find(im>Skin_low & im<Skin_high)) = 1; 
    
    subplot(1,length(Pict_cell)+3,ii+1,'Parent',handles.uipanel1);
    imagesc(skinmask); title('Skin mask')
    axis equal
    
    PVSmask = im*0;
    PVSmask(find(im>PVS_low & im<PVS_high)) = 1; 
    
    subplot(1,length(Pict_cell)+3,ii+2,'Parent',handles.uipanel1);
    imagesc(PVSmask); title ('PVS mask')
    axis equal
    
    Ringmask = im*0;
    Ringmask(find(im>Ring_low & im<Ring_high)) = 1; 
    
    subplot(1,length(Pict_cell)+3,ii+3,'Parent',handles.uipanel1);
    imagesc(Ringmask); title ('Ring mask')
    axis equal
    

    handles.Thresholds = struct('Skin_high',Skin_high,'Skin_low',Skin_low,'PVS_high', PVS_high,...
        'PVS_low', PVS_low, 'Ring_high', Ring_high, 'Ring_low', Ring_low)
    
    guidata(hObject,handles)
end


